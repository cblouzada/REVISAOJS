
let num1 = Number(prompt("1 - Insira um numero"));
let num2 = Number(prompt("2 - Insira um numero"));

console.log(`Os numero digitados são: ${num1} e ${num2}`);

resultadoA = num1 + num2;
resultadoB = num1 * num2;
resultadoC = num1 - num2;
resultadoD = num1 / num2;
resultadoE = num1 % num2;


console.log(`Soma: ${num1} + ${num2} = ${resultadoA}`);
console.log(`Multiplicação: ${num1} * ${num2} = ${resultadoB}`);
console.log(`Subtração: ${num1} - ${num2} = ${resultadoC}`);
console.log(`Divisão: ${num1} / ${num2} = ${resultadoD}`);
console.log(`Resto: ${num1} % ${num2} = ${resultadoE}`);



